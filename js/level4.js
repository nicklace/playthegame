// Ajoute la fonction de victoire spécifique au niveau du boss
LEVEL4.victory = function(engine) {
    // On vérifie si le boss a été vaincu
    const boss = engine.entities.find(entity => entity instanceof Boss);
    if (!boss) {
        // Si le boss n'existe plus (vaincu) et le joueur a récupéré le confetti
        const confetti = engine.entities.find(entity => 
            entity instanceof Collectible && entity.collectibleType === 'confetti');
        
        if (!confetti) {
            // Le confetti a été collecté, le joueur a gagné
            return true;
        }
    }
    return false;
};

